 I=dicomread('IM2');%image radiographique à dose reduite
 imshow(I(:,:,1));
 R=I(:,:,1);
 axes(handles.axes2);
 I2=dicomread('IM3');%image radiographique à dose de reference
 imshow(I2(:,:,1));
 Ref=I2(:,:,1);
 [T,rectT]=imcrop(R);
  mailleR=T;coord=rectT;
  mailleRef=imcrop(Ref,rectT);
  
  axes(handles.axes2);
  subplot(2,2,1),imshow(mailleR), title('Dose réduite');
  subplot(2,2,2),imshow(mailleRef),title('Dose de référence');
  
  moy=mean2(mailleR);
  moysR = num2str(moy);
  handles.edit1.String = moysR;
  
  moy2=mean2(mailleRef);
  moysREF = num2str(moy2);
  handles.edit2.String = moysREF;
  
  var=std2(mailleR);
   varsR = num2str(var);
  handles.edit3.String = varsR;
  
  var2=std2(mailleRef);
  varsREF = num2str(var2);
  handles.edit4.String = varsREF;
  
   GLCM = graycomatrix(mailleR,'Offset',[1 0]);
   stats = graycoprops(GLCM,'all');
   contrast = stats.Contrast;
  correlation = stats.Correlation;
  energie = stats.Energy;
   homogeneite = stats.Homogeneity;
   
    GLCM2 = graycomatrix(mailleRef,'Offset',[1 0]);
   stats2 = graycoprops(GLCM2,'all');
   contrast2 = stats2.Contrast;
  correlation2 = stats2.Correlation;
  energie2 = stats2.Energy;
  homogeneite2 = stats2.Homogeneity;
  
   
   hom = num2str(homogeneite);
   handles.edit5.String = hom;
  
  
   hom2 = num2str(homogeneite2);
   handles.edit6.String = hom2;
   
   con = num2str(contrast);
   handles.edit7.String = con;
   
   con2 = num2str(contrast2);
   handles.edit8.String = con2;
   
   cor = num2str(correlation);
   handles.edit9.String = cor;
   
   cor2 = num2str(correlation2);
   handles.edit10.String = cor2;
   
    ene = num2str(energie);
   handles.edit11.String = ene;
   
   ene2 = num2str(energie2);
   handles.edit12.String = ene2;
   
   
  
 return;
  


